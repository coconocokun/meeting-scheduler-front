  export const toString = (value: boolean[][]) => {
    let str = "";
    for (let i = 0; i < value.length; i++) {
      for (let j = 0; j < value[i].length; j++) {
        const element = value[i][j];
        if (element == true) {
          const num = 48 * j + i;
          str = str + num + ",";
        }
      }
    }
    str = str.slice(0, -1);
    return str;
  };


